#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (n-- > 0)
	{
		if (*(char *)s1 != *(char *)s2)
			return (*(char *)s2 - *(char *)s1);
		s1++;
		s2++;
	}
	return (0);
}
