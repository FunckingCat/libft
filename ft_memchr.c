#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	if (!(char *)s)
		return (NULL);
	if (n == 0)
		return (NULL);
	while (n-- > 0)
	{
		if (*(char *)s == (char)c)
			return ((void *)s);
		s++;
		n--;
	}
	return (NULL);
}
